puts ""
# Print vs. Puts
# The difference between 'print' and 'puts' is that 'puts'
# adds a newline character at the end of the string, while 'print' does not.
# Example:
puts "Print vs. Puts"
puts "Line 1 using puts"
print "Line 2 using print"
puts "Line 3 using puts"
puts ""
# Output:
# Line 1 using puts
# Line 2 using printLine 3 using puts

